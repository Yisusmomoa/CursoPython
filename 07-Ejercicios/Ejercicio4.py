'''
Ejercicio4 pedir dos numeros al usuario y hacer todas las operaciones basicas de una calculadora y mostrarlo en pantalla
'''


numero1=int(input("Captura el primer numero: "))
numero2=int(input("Captura el segundo numero: "))

suma=numero1+numero2
resta=numero1-numero2
multiplicacion=numero1*numero2
division=numero1/numero2

print(f"La suma es: {suma}")
print(f"La resta es: {resta}")
print(f"La multiplicacion es: {multiplicacion}")
print(f"La division es: {division}")
